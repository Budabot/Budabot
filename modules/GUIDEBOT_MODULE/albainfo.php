<font color = green>Albtraum Quests</font>

<font color = yellow>Finding answers</font>

You will need to chat with <font color = yellow>The One that Sees Dead People</font>.  
He will give you the mission that is needed to enter the portal.  He senses a great evil inside Albtraum, and will have you investigate it.  
He will ask that you kill a number of creatures for him, and return some of their parts for him (which you will receive once you get the required number of kills).  You will need to kill 4 Shadow Eremites, 12 Soul Stealers, 15 Shadowy Wings and 20 Voracious Horrors.  There are plenty of horrors, wings, and eremites.  The Soul Stealers however are the rarest of the zone, and you might need to kill every one inside the zone to reach the required 12.  
The yuttos asks you to return to him the following parts : 

<font color = green>Morphing Eremite Spinal Section
Crystallizd Residue
Dark Tooth 
Trasparaent muscle strands</font>
You will get these automatically when you complete the kills.

You must be teamed in order to receive the mission.  
Each member of the team doesn't need to get the quest,but it helps as each will need to reward from it.  
Once everyone has the mission, it will be time to enter Albtraum.  Simply walk on the portal.

Once inside, you will need to start to kill your required mobs.  This should be straightforward enough.  
With a decent team, you don't need to worry about adds too much.  
One thing to keep in mind is that the horrors and Soul Stealers have a chance of spawning an extra spirit mob once you kill them.  
This can be dangerous if you're in a hurry and pull some more before it spawns.  
It may be a good idea to wait after you kill any horror or soul stealer.


Once you kill all the required mobs and get your items, return to <font color = yellow>The One that Sees Dead People.</font>
<font color = green>Make sure that at least one person stays inside the playfield, as you do not want it to reset.</font>  
If this happens, you will have to re-kill the mobs that you killed previously.  
While you won't have to redo the mission, it will still be bothersome to have to kill everything again.

<font color = green>Preparing for the Final Battle</font>


<font color = yellow>The One that Sees Dead People</font> will tell you to go talk to <font color = green>One Whose Courage Fails His Heart.</font>
This yutto can be found at the entrance of Albtraum.  Only one person needs to talk to the him.  
He will instruct you to go collect 3 parts to make a novictum bomb : 
<font color = yellow>a perennium shell
a vial of highly volatile novictum
and a detonator.</font>  

You will find these on 3 bosses that have just spawned inside the playfield.  
If you kept the playfield, you will be able to walk straight to the bosses without having to fight any mobs.  
If you lost the playfield, 'The little one' will despawn most mobs. Only ones that stay are wanders and eremites.


When you kill the bosses, they will drop a variety of items, as well as 4 identical parts needed to make the bomb.  That means only 4 people will be able to make the bomb for each playfield.  
If you want the entire team to receive the bomb (which *might* be needed) you will have to clear 2 playfields.

<font color = Red>The Mini Bosses</font>

<font color = yellow>Mutant Shadow Eremite</font>

This first boss is located on the east side of the zone.  If you stay on the east wall, you will come to a opening.  
He is hidden in the ground just at the entrance of that opening.  Make sure that your team is ready before going near it.

This boss shouldn't pose too many problems, he has an AoE stun, which can be annoying.  
Have your doc and any ranged non-tankers stay back.  The rest, just have plenty of Free Movement Stims ready, and you should be fine.

<font color = yellow>Wings of Despair</font>

Further north from the eremite, you will find the Wings of Despair.  
This Boss will be a bit harder than the Eremite.  He has an AoE nano drain.  
It appears to be quite vast, as we couldn't get the doc far enough to get out of its range.  
Keep some nano stims handy, and make sure to stim your doc when needed.  He will need them.

<font color = yellow>Overwhelming Horror</font>

This will be your most difficult challenge yet.  This horror is very difficult.  
<font color = yellow>He has a nasty 18k nuke</font>, which will decimate your team quickly if you're not careful.  
It would be a good idea to have your tank on him, and have everyone else way back, attacking him from afar.  
Unless he has been tuned down since seen on testlive (this will remain to be seen), he nukes often enough.  
So it may be a good idea to have your melee classes sit this one out, with the exception of you enforcer if he is tanking of course.

Again, let me remind you that you need to have 1 person alive in order to keep the playfield.  
If your entire team wipes, while you won't have to start the mission over, you will have to start on a new playfield, with all mobs spawned.  
If you see that this battle is not going well, it may be a good idea run away to escape his aggro, this will enable your team to return to the same playfield and try again.  Do not take this boss lightly.

Once you receive your 3 bomb parts, return to The One Who Sees Dead People.  
He will make a Novictum Bomb for you, and instruct you to talk to the yuttos inside the playfield again.


<font color =red>Warning!</font> Do not right click the bomb, or use anything (tradeskill) on it.  
If you do this, it will explode and will be used up.  If you do this, you will have to start over.

<font color = yellow>The Final Battle</font>

Note:At this point you can reset field (by having every team member leave playfield), but little Yutto will slack again and not despawn all mobs. 
So better strategy is clearing whole playfield after killing bomb bosses and leaving at least one team member inside to keep playfield.

Once you talk to the Yuttos inside the playfield, the last boss, <font color = green>The Keeper of Tortured Souls</font>, will appear.  
You will find him at the end of the playfield, near the statue.  This boss is extremelly difficult, so be prepared for a wipe if team is inexperienced.  

Keeper has a whole arsenal of nasty tricks up his sleeve. 
He stuns when near its victim, AoE nukes, doesn't keep agro and of course spawns bunch of spirit Soul Stealers. 
They are low level (205), but can still be dangerous if they gang-bang on someone. 
But they can be disposed with bomb(s) you got earlier. 
Since they too can drop phats, we advise to keep them alive till Keeper is dead and then use bombs (plural, since they don't have that great AoE effect, so more then likely you wont catch them all with just one) and quickly check their loot.

Best strategy found so far for killing keeper is kiting. 
Soldiers exceed in this field, especially if they have back-up of professions that can stun and root (so that asault force can shot at it from safe distance). 
And since even this strategy can go wrong, and MP or Engi in team is advised to warp dead raiders back. 
Warper must think ahead tho and anchor team in a safe location and stay out of fight (Keeper doesn't hold agro, remember). 

